    private void llenarTabla() {
        DefaultTableModel modelo = (DefaultTableModel) JTEmpleados.getModel();
        modelo.setRowCount(0);
        DAOEmpleado dao = new DAOEmpleado();
        for (PojoEmpleado emp : dao.findAll()) {
            modelo.addRow(new Object[]{
                emp.getClave(),
                emp.getNombre(),
                emp.getDireccion(),
                emp.getTelefono()
            });
        }
    }
    
    private Long seleccionarFila(){
        int fila = JTEmpleados.getSelectedRow();
        
        if(fila > -1){
             Object claveObj = JTEmpleados.getValueAt(fila, 0);
             return Long.parseLong(claveObj.toString());
        }else{
            return null;
        }
    }

