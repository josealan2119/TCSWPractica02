    private void JBMostrar_DatosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBMostrar_DatosActionPerformed
        Long clave = seleccionarFila();
        if(clave != null){
            EmpleadoDatosGUI gui = new EmpleadoDatosGUI(clave);
            desktoppane.add(gui);
            gui.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(null,"Selecciona una fila");
        }
    }//GEN-LAST:event_JBMostrar_DatosActionPerformed

    private void JBEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBEliminarActionPerformed
        Long clave = seleccionarFila();
        if(clave != null){
            int respuesta = JOptionPane.showConfirmDialog(null,
                "Desea elimiar este Empleado?",
                "Confirmas Elimicacion",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.WARNING_MESSAGE);
        if(respuesta == JOptionPane.YES_OPTION){
            DAOEmpleado dao = new DAOEmpleado();
            if(dao.eliminar(clave) != null){
                JOptionPane.showMessageDialog(this,"Se elimino correctamente");
                llenarTabla();
            }else{
                JOptionPane.showMessageDialog(this,"Error al Eliminar");
            }
        }else{
            JOptionPane.showMessageDialog(null,"Cancelacion Exitosa");
        }
        }else{
            JOptionPane.showMessageDialog(null,"Selecciona una fila");
        }
    }//GEN-LAST:event_JBEliminarActionPerformed


